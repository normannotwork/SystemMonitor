#!/bin/bash
set -e  # Остановка скрипта при любой ошибке

echo "🚀 Запуск полной сборки SysMon..."
echo "-----------------------------------"

# =============================================================================
# 🎨 ЧАСТЬ 1: Сборка Фронтенда
# =============================================================================
echo "[1/4] Проверка директории frontend..."
if [ ! -d "frontend" ]; then
  echo "❌ Ошибка: директория 'frontend' не найдена!"
  exit 1
fi

cd frontend

echo "[2/4] Установка зависимостей и сборка TS..."
npm install --silent
npm run build --silent

echo "[3/4] Подготовка статических файлов (dist)..."
mkdir -p dist
cp src/index.html src/style.css dist/
# Переименовываем main.js в app.js, если он существует, чтобы соответствовать ожиданиям сервера
mv dist/main.js dist/app.js 2>/dev/null || true

cd ..
echo "✅ Фронтенд собран успешно."
echo "-----------------------------------"

# =============================================================================
# ⚙️ ЧАСТЬ 2: Сборка Бэкенда (C++17)
# =============================================================================
echo "[4/4] Сборка бэкенда (CMake + Make)..."

mkdir -p build
cd build

cmake .. > /dev/null 2>&1
if [ $? -ne 0 ]; then
    echo "❌ Ошибка конфигурации CMake!"
    exit 1
fi

make -j$(nproc)
if [ $? -ne 0 ]; then
    echo "❌ Ошибка компиляции Make!"
    exit 1
fi

cd ..

echo "-----------------------------------"
echo "🎉 Сборка завершена успешно!"
echo ""
echo "Для запуска выполните:"
echo "   ./build/sysmon"
echo ""
echo "Откройте в браузере: http://localhost:8080"
